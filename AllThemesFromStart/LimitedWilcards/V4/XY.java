package AllThemesFromStart.LimitedWilcards.V4;

public class XY {
    private final Integer x;
    private final Integer y;

    public XY(int x, int y){
        this.x = x;
        this.y = y;
    }

    public Integer getX(){
        return x;
    }

    public Integer getY(){
        return y;
    }
}
